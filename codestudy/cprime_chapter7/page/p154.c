// 程序功能：处理输入字符，空格原样输出，其他字符 ASCII 码 +1 输出  
// 知识点覆盖：getchar/putchar 用法、while 循环、if-else 分支、字符编码操作  
#include <stdio.h>  

// 定义空格常量（字符单引号包裹，对应 ASCII 空格值）  
#define SPACE ' '  

int main(void) {  
    char ch;  

    // 1. getchar() 读入一个字符，从标准输入流获取用户输入  
    ch = getchar();  

    // 2. while 循环：判断是否到达行末尾（'\n' 是换行符，代表一行输入结束）  
    //    这里体现 "循环持续到输入结束" 的逻辑，类似教材里多桌面流程控制的 "持续执行" 思想  
    while (ch != '\n') {  
        // 3. if-else 分支：判断是否为空格  
        if (ch == SPACE) {  
            // 3.1 是空格则原样输出，用 putchar 直接打印  
            putchar(ch);  
        } else {  
            // 3.2 非空格字符：ASCII 码 +1 后输出  
            //    字符本质是整数，+1 会改变其编码值（比如 'A' 变成 'B' ）  
            putchar(ch + 1);  
        }  

        // 4. 读取下一个字符，让循环能持续处理输入的每一个字符  
        ch = getchar();  
    }  

    // 5. 打印换行符，让输出结果换行，和输入的行结构对应  
    putchar(ch);  

    return 0;  
}  

