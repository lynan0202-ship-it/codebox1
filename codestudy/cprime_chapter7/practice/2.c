/*
1. 题目问题：编写程序读取输入，读到字符停止，打印每个输入字符及对应的 ASCII 码（十进制），每行打印 8 个“字符 - ASCII 码”组合。
2. 问题要点与知识点：用 getchar() 读取字符，借助计数器和模运算控制每行打印数量，涉及字符处理、循环、模运算、ASCII 码知识。
*/

#include <stdio.h>

int main() {
    char ch;
    int count = 0;  // 用于计数，控制每行打印个数
    while ((ch = getchar()) != EOF && ch != '\n') {  // 假设遇到换行或文件结束停止（可按需求调整停止条件）
        printf("%c-%d\t", ch, ch);  // 打印字符和对应的 ASCII 码
        count++;
        if (count % 8 == 0) {  // 每 8 个换一行
            printf("\n");
        }
    }
    if (count % 8 != 0) {  // 最后一行不足 8 个时补换行
        printf("\n");
    }
    return 0;
}
