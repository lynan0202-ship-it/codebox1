int main() {
    int num = 12345; // 要存储的整数
    
    // ---- 文本文件存储：转成字符串 "12345" ----
    FILE *txt_file = fopen("text_mode.txt", "w");
    fprintf(txt_file, "%d", num); // 按字符形式写入
    fclose(txt_file);
    
    // ---- 二进制文件存储：直接写内存中的4字节 ----
    FILE *bin_file = fopen("bin_mode.bin", "wb");
    fwrite(&num, sizeof(int), 1, bin_file); // 写int的原始字节
    fclose(bin_file);
    
    printf("已生成两个文件：\n");
    printf("- text_mode.txt（文本，存字符）\n");
    printf("- bin_mode.bin（二进制，存原始字节）\n");
    return 0;
}

