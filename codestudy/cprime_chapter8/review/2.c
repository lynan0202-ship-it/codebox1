/* 
问题2题干：下面的语句分别完成什么任务？
a. putchar('H'); 
b. putchar('\007');
c. putchar('\n'); 
d. putchar('\b');

知识要点：
1. 转义字符含义：
   - '\007'：响铃符（BEL）
   - '\n'：换行符（LF）
   - '\b'：退格符（BS）
2. putchar() 基本功能：输出单个字符

总体逻辑：
逐个演示转义字符和普通字符的输出效果，观察终端响应

代码功能：
按顺序执行四条putchar语句，直观展示每个字符的作用
*/

#include <stdio.h>

int main() {
    // a. 输出普通字符'H'
    putchar('H');
    putchar('\n'); // 换行，方便区分结果
    
    // b. 输出响铃符（终端可能发声或显示特殊标记）
    putchar('\007');
    putchar('\n'); // 换行
    
    // c. 输出换行符（光标移至下一行开头）
    putchar('\n');
    
    // d. 输出退格符（尝试覆盖前一个字符）
    putchar('A'); // 先输出'A'
    putchar('\b'); // 退格
    putchar('B'); // 覆盖为'B'
    putchar('\n'); // 换行

    return 0;
}

