// 题干：用数组表示法表示 a 声明的数组的第 3 个元素（其值为 4） 
// 知识要点：
//  1. 数组元素的访问：通过数组名 + 下标（索引）的方式访问数组元素，下标从 0 开始
// 总体逻辑：
//  1. 基于题目 8a 中声明的数组，使用数组表示法访问第 3 个元素（注意下标是 2 ，因为从 0 开始 ）
#include <stdio.h>
int main() {
    // 先声明并初始化数组，同题目 8a 
    int arr[6] = {1, 2, 4, 8, 16, 32};
    // 数组表示法访问第 3 个元素（下标为 2 ）
    int thirdElement = arr[2];
    printf("数组的第 3 个元素（值为 4）用数组表示法访问结果：%d\n", thirdElement);
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译并运行代码
//  2. 预期结果：输出 4 ，即数组的第 3 个元素值
//  3. 检查点：看输出是否为 4 ，验证数组表示法的正确性
// 易错点提醒：
//  1. 容易把下标写错，比如写成 arr[3]  ，要牢记数组下标从 0 开始，第 3 个元素下标是 2 
