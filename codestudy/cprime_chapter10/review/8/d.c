// 题干：假设编译器支持 C99/C11 标准，声明一个内含 100 个 int 类型值的数组，并初始化下标为 5、10、11、12、13 的元素为 101，其他元素不考虑 
// 知识要点：
//  1. 数组的部分初始化：利用 C99/C11 标准支持的指定初始化器，可初始化多个指定下标的元素
// 总体逻辑：
//  1. 声明 int 类型数组，元素个数为 100 
//  2. 使用初始化列表，通过指定下标初始化对应元素为 101 
#include <stdio.h>
int main() {
    // 声明数组 arr ，元素个数 100 ，初始化指定下标元素为 101 
    int arr[100] = {[5] = 101, [10] = 101, [11] = 101, [12] = 101, [13] = 101};
    // 输出这些下标对应的元素，验证初始化
    printf("arr[5] = %d\n", arr[5]);
    printf("arr[10] = %d\n", arr[10]);
    printf("arr[11] = %d\n", arr[11]);
    printf("arr[12] = %d\n", arr[12]);
    printf("arr[13] = %d\n", arr[13]);
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译并运行代码（需确保编译器支持 C99/C11 标准，如 gcc 用 -std=c99 选项 ）
//  2. 预期结果：输出的对应下标元素值都是 101 
//  3. 检查点：看输出的每个值是否为 101 ，验证部分初始化的正确性
// 易错点提醒：
//  1. 容易写错下标，要仔细核对题目要求的下标值 
//  2. 编译器不支持 C99/C11 标准时，这种初始化方式会报错，要注意编译器设置
