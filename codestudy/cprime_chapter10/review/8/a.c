// 题干：声明一个内含 6 个 int 类型值的数组，并初始化各元素为 1、2、4、8、16、32 
// 知识要点：
//  1. 数组的初始化：在声明数组时，用大括号 {} 初始化元素值
//  2. 数组元素个数与初始化列表的匹配：初始化列表元素个数应与数组声明的元素个数一致
// 总体逻辑：
//  1. 声明 int 类型数组，指定元素个数为 6 
//  2. 使用初始化列表为数组元素赋值
#include <stdio.h>
int main() {
    // 声明并初始化数组 arr ，元素个数为 6 ，值分别是 1、2、4、8、16、32 
    int arr[6] = {1, 2, 4, 8, 16, 32};
    for (int i = 0; i < 6; i++) {
        printf("arr[%d] = %d\n", i, arr[i]);
    }
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译并运行代码（如 gcc -o test test.c  ，然后运行 ./test ）
//  2. 预期结果：输出数组各元素的值，依次为 1、2、4、8、16、32 
//  3. 检查点：看输出的数组元素是否与初始化列表一致
// 易错点提醒：
//  1. 容易写错初始化列表的元素值，比如把 32 写成其他数值
//  2. 容易把数组元素个数声明错误，比如声明成 int arr[5];  ，导致初始化列表元素个数不匹配
// 拓展思考：
//  1. 如果要初始化一个元素个数很多的数组，手动写初始化列表很麻烦，有什么其他方法？（比如利用循环赋值 ）
// 对比说明：
//  - 这种显式初始化数组的方式适合元素个数少且值确定的情况，与动态初始化（如循环赋值 ）相比，更直接但缺乏灵活性 