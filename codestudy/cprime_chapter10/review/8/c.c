// 题干：假设编译器支持 C99/C11 标准，声明一个内含 100 个 int 类型值的数组，并初始化最后一个元素为 -1，其他元素不考虑 
// 知识要点：
//  1. 变长数组（VLA）：C99/C11 标准支持，但本题不是变长数组，而是普通数组的部分初始化 
//  2. 数组的部分初始化：在初始化列表中，可只初始化部分元素，未初始化的元素默认值为 0（对于 int 类型数组 ），这里利用部分初始化特性初始化最后一个元素
// 总体逻辑：
//  1. 声明 int 类型数组，元素个数为 100 
//  2. 使用初始化列表，只初始化最后一个元素为 -1 
#include <stdio.h>
int main() {
    // 声明数组 arr ，元素个数 100 ，初始化最后一个元素（下标 99 ）为 -1 
    int arr[100] = {[99] = -1};
    // 输出最后一个元素，验证初始化
    printf("数组最后一个元素的值：%d\n", arr[99]);
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译并运行代码（需确保编译器支持 C99/C11 标准，如 gcc 用 -std=c99 选项 ）
//  2. 预期结果：输出 -1 ，即最后一个元素的值
//  3. 检查点：看输出是否为 -1 ，验证部分初始化的正确性
// 易错点提醒：
//  1. 容易写错下标，比如把 [99] 写成 [100]  ，要注意数组下标范围是 0 到 99 （共 100 个元素 ）
//  2. 编译器不支持 C99/C11 标准时，这种部分初始化方式可能无法使用，要注意编译器设置
