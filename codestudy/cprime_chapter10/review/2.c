// 题干：在复习题 1 中，ref 有多少个元素？
// 知识要点：
//  1. 数组元素个数的确定：当数组初始化时，若没有指定大小，元素个数由初始化列表中的元素数量决定
//  2. 数组的基本概念：数组是一组相同类型数据的集合
// 总体逻辑：
//  1. 查看复习题 1 中数组 ref 的初始化列表
//  2. 统计初始化列表中的元素数量，确定 ref 的元素个数
#include <stdio.h>
int main() {
    // 定义数组 ref，根据初始化列表确定元素个数
    int ref[] = {8, 4, 0, 2};
    // 计算数组元素个数，sizeof(ref)是数组总字节数，sizeof(ref[0])是单个元素字节数
    int elementCount = sizeof(ref) / sizeof(ref[0]);
    printf("ref 的元素个数是：%d\n", elementCount);
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译该代码（如使用 gcc 命令：gcc -o test test.c ，然后运行 ./test ）
//  2. 预期结果：应该输出 ref 的元素个数为 4
//  3. 检查点：重点看输出的数字是否为 4
// 易错点提醒：
//  1. 容易错误地认为数组元素个数是其他数值，要牢记根据初始化列表统计
// 拓展思考：
//  1. 如果数组是动态分配内存的（如 int *ref = (int *)malloc(5 * sizeof(int)); ），还能这样计算元素个数吗？为什么？
// 对比说明：
//  - 静态数组（本题这种初始化方式）：可通过 sizeof 计算元素个数；动态分配的数组：不能用这种方式，因为 sizeof 得到的是指针的大小，而非数组总字节数