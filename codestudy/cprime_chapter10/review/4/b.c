// 题干：在下面的代码中，*ptr 和*(ptr + 2)的值分别是什么？
//      b.
//      int * ptr;
//      int fort[2][2] = { {12}, {14,16} };
//      ptr = fort[0];
// 知识要点：
//  1. 二维数组的初始化：初始化列表可部分初始化，未初始化元素为 0
//  2. 指针与二维数组：指针指向二维数组的行，访问元素时按内存布局
//  3. 指针算术运算：指针移动的偏移规则
// 总体逻辑：
//  1. 分析二维数组 fort 的存储布局，确定各元素值
//  2. 确定 ptr 指向位置，计算 *ptr 和*(ptr + 2)的值
#include <stdio.h>
int main() {
    int *ptr;
    // 二维数组 fort 存储：第一行 {12, 0} ，第二行 {14,16} 
    int fort[2][2] = { {12}, {14,16} };
    ptr = fort[0];
    printf("*ptr 的值是：%d\n", *ptr);
    printf("*(ptr + 2) 的值是：%d\n", *(ptr + 2));
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译运行代码
//  2. 预期结果：*ptr 为 12 ，*(ptr + 2) 为 14 
//  3. 检查点：看输出是否符合预期
// 易错点提醒：
//  1. 容易忽略二维数组中未显式初始化元素的值为 0 ，导致计算错误
// 拓展思考：
//  1. 如果 fort 的初始化列表是 { {12,13}, {14} } ，结果会怎样？
// 对比说明：
//  - 不同的二维数组初始化方式会导致内存中元素存储不同，指针访问结果也不同，要准确分析初始化后的存储情况