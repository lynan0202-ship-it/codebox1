// 题干：在下面的代码中，**ptr 和**(ptr + 1)的值分别是什么？
//      a.
//      int (*ptr)[2];
//      int torf[2][2] = {12, 14, 16};
//      ptr = torf;
// 知识要点：
//  1. 指向数组的指针：这种指针指向一个包含指定个数元素的数组
//  2. 二维数组与指向数组的指针：指向数组的指针可指向二维数组的行
//  3. 指针的解引用：多次解引用访问二维数组元素
// 总体逻辑：
//  1. 分析指向数组的指针 ptr 的指向
//  2. 通过解引用指针计算 **ptr 和**(ptr + 1)的值
#include <stdio.h>
int main() {
    // ptr 是指向包含 2 个 int 元素的数组的指针
    int (*ptr)[2];
    // 二维数组 torf 存储为按行连续：12,14,16, （剩余元素默认初始化，这里为 0  ）
    int torf[2][2] = {12, 14, 16};
    ptr = torf;
    printf("**ptr 的值是：%d\n", **ptr);
    printf("**(ptr + 1) 的值是：%d\n", **(ptr + 1));
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译运行代码
//  2. 预期结果：**ptr 为 12 ，**(ptr + 1) 为 16 
//  3. 检查点：看输出是否符合预期
// 易错点提醒：
//  1. 容易混淆指向数组的指针的解引用方式，要牢记多次解引用的步骤
// 拓展思考：
//  1. 如果 ptr 指向的数组元素个数改变（如 int (*ptr)[3]; ），结果会怎样变化？
// 对比说明：
//  - 指向数组的指针与普通指针不同，其移动和解引用是基于所指向数组的大小，和普通指针的算术运算有区别