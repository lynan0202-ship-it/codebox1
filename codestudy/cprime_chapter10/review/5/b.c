//      b.
//      int (*ptr)[2];
//      int fort[2][2] = { {12}, {14,16} };
//      ptr = fort;
// 知识要点：
//  1. 指向数组的指针的使用：操作二维数组的行
//  2. 二维数组的初始化与元素存储：不同初始化方式下元素的存储情况
//  3. 多次解引用指向数组的指针访问元素
// 总体逻辑：
//  1. 确定指向数组的指针 ptr 的指向
//  2. 解引用指针计算 **ptr 和**(ptr + 1)的值
#include <stdio.h>
int main() {
    // ptr 是指向包含 2 个 int 元素的数组的指针
    int (*ptr)[2];
    // 二维数组 fort 存储：第一行 {12, 0} ，第二行 {14,16} 
    int fort[2][2] = { {12}, {14,16} };
    ptr = fort;
    printf("**ptr 的值是：%d\n", **ptr);
    printf("**(ptr + 1) 的值是：%d\n", **(ptr + 1));
    return 0;
}
// 测试验证方案：
//  1. 怎么运行：编译运行代码
//  2. 预期结果：**ptr 为 12 ，**(ptr + 1) 为 14 
//  3. 检查点：看输出是否符合预期
// 易错点提醒：
//  1. 容易错误判断二维数组中未显式初始化元素的值，要注意默认初始化规则
// 拓展思考：
