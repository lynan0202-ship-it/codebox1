//  1. 怎么运行：编译代码（如使用 gcc 命令：gcc -o test test.c ，然后运行 ./test ）
//  2. 预期结果：原数组1.10 2.20 3.30 4.40 5.50，倒序后为5.50 4.40 3.30 2.20 1.10
//  3. 检查点：看输出的数组元素顺序是否正确，可修改数组元素重新测试
// 易错点提醒：
//  1. 容易在交换元素时忘记使用临时变量temp，导致元素值丢失
//  2. 循环条件容易写错，应该是i < len / 2，否则会重复交换，回到原数组
