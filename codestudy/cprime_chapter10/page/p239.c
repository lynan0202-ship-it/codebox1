//  2. 初始化数组 days 存储每个月的天数，并用 const 演示只读用法（可根据需求切换）
//  3. 通过 for 循环遍历数组，输出 "Month X has XX days" 格式的结果
//  4. 演示两种数组用法：普通数组（可修改）和 const 数组（只读）
// ------------------------------

#include <stdio.h>
// 定义符号常量，代表一年的月份数量
#define MONTHS 12  

int main(void)
{
    // 普通数组用法：存储每个月的天数（未考虑闰年，2月固定28天）
    // 下标 0~11 对应 1~12月，元素值依次为各月天数
    int days[MONTHS] = {31, 28, 31, 30, 31, 30, 
                        31, 31, 30, 31, 30, 31};  
    
    // const 数组用法演示（扩展）：
    // 一旦初始化，程序运行中不能修改数组内容
    // const int days[MONTHS] = {31,28,31,30,31,30,31,31,30,31,30,31};

    int index;
    // 遍历数组：index 从 0 到 11（对应 12 个月）
    for (index = 0; index < MONTHS; index++)  
    {
        // index+1 转换为真实月份（1~12），days[index] 是当月天数
        printf("Month %2d has %2d days.\n", index + 1, days[index]);  
    }

    return 0;
}

