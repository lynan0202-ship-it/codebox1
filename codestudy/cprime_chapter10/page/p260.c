/*
知识要点总结：
1. 二维数组与指针的关系：二维数组名是其首元素（内含若干元素的一维数组）的地址；数组名的值和其首元素一维数组的地址（&数组名[0]）相同，一维数组名的值和其首元素地址（&数组名[0][0]）相同 。
2. 指针运算：对二维数组名或指向一维数组的指针进行加减运算，步长是对应元素（一维数组或单个元素）的大小；解引用指针可获取对应元素的值，多层解引用可获取二维数组中具体元素值 。
3. 多种访问二维数组元素的方式：可通过数组下标（如 zippo[2][1] ）、指针解引用（如 *(*(zippo + 2) + 1) ）等方式访问，本质是对指针运算和地址解析的不同应用 。

代码总体逻辑说明：
1. 定义二维数组 zippo 并初始化，它是内含 4 个一维数组（每个一维数组含 2 个 int 元素 ）的二维数组 。
2. 通过一系列 printf 语句，分别打印二维数组名、指向一维数组的指针、指针解引用等表达式的值或地址，展示二维数组名与指针的关系、指针运算结果、不同方式访问数组元素的效果，帮助理解二维数组的指针操作逻辑 。
*/

#include <stdio.h>

int main(void)
{
    // 定义并初始化二维数组 zippo，它有 4 个一维数组元素，每个一维数组含 2 个 int 值
    int zippo[4][2] = { {2, 4}, {6, 8}, {1, 3}, {5, 7} };

    // zippo 是二维数组首元素（即第一个一维数组 zippo[0] ）的地址，打印其地址值；zippo + 1 是下一个一维数组的地址
    printf("   zippo = %p,   zippo + 1 = %p\n", zippo, zippo + 1);
    // zippo[0] 是第一个一维数组的数组名，它代表该一维数组首元素（zippo[0][0] ）的地址，和 &zippo[0] 值相同；zippo[0] + 1 是该一维数组下一个元素的地址
    printf("zippo[0] = %p, zippo[0] + 1 = %p\n", zippo[0], zippo[0] + 1);
    // *zippo 等价于 zippo[0] ，是第一个一维数组首元素（zippo[0][0] ）的地址；*zippo + 1 是该一维数组下一个元素的地址
    printf("  *zippo = %p,   *zippo + 1 = %p\n", *zippo, *zippo + 1);
    // zippo[0][0] 是二维数组具体元素，取地址后打印，和 *zippo、zippo[0] 等表示的地址呼应
    printf("zippo[0][0] = %p\n", &zippo[0][0]);
    // 通过数组下标方式访问并打印元素值，zippo[0][0] 是二维数组第一行第一列元素
    printf("  zippo[0][0] = %d\n", zippo[0][0]);
    // *zippo 是 zippo[0][0] 的地址，解引用一次得到 zippo[0][0] 的值（等价于 **zippo ）
    printf("    *zippo[0] = %d\n", *zippo[0]);
    // **zippo 先解引用 zippo 得到 zippo[0] （一维数组名，也是地址 ），再解引用得到 zippo[0][0] 的值
    printf("     **zippo = %d\n", **zippo);
    // zippo + 2 指向第三个一维数组（从 0 开始计数 ），解引用得到该一维数组首元素地址，再解引用得到该一维数组第一个元素值（即 zippo[2][0] ）
    printf("  *(*(zippo+2)) = %d\n", *(*(zippo + 2)));
    // 先找到 zippo[2] 这个一维数组，再取其第二个元素（下标 1 ）的值，即 zippo[2][1]
    printf("  zippo[2][1] = %d\n", zippo[2][1]);
    // 通过指针运算方式，zippo + 2 指向第三个一维数组，解引用得到其首元素地址，加 1 指向该一维数组第二个元素，再解引用得到值（和 zippo[2][1] 等价 ）
    printf("*(*(zippo+2) + 1) = %d\n", *(*(zippo + 2) + 1));

    return 0;
}

