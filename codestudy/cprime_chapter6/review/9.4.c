#include <stdio.h>
int main(void)
{
    int n, m;

    // 片段 1：while 循环
    n = 30;
    // 条件 30 <= 20 为假，循环体不执行
    while (n <= 20)  
        printf("%d|", n);

    // 片段 2：do-while 循环
    n = 30;
    do
        printf("%d|", n);  // 先执行一次，打印 30|
    // 每次循环先将 n 自增，再判断（31→33 满足条件，34 不满足）
    while (++n <= 33); 

    printf("\n***\n");

    // 片段 3：for 循环（n从1开始，每次+4，直到n²≥200）
    for (n = 1; n*n < 200; n += 4) 
    {
        printf("%d\n", n); 
    }

    printf("\n***\n");

    // 片段 4：for 循环（n初始2，m初始6；n < m时，n乘2，m加2）
    for (n = 2, m = 6; n < m; n *= 2, m += 2) 
    {
        printf("%d %d\n", n, m); 
    }

    printf("\n***\n");

    // 修正后的嵌套循环：外层n从5递减到1，内层打印n个"="
    for (n = 5; n > 0; n--)  // 外层循环：控制行数（5行→1行）
    {
        // 内层循环：使用独立变量m，每次从1开始，打印n个"="
        for (m = 1; m <= n; m++)  // m初始化为1，避免未定义行为
        {
            printf("=");
        }
        printf("\n");  // 每行结束后换行
    }

    return 0;
}
