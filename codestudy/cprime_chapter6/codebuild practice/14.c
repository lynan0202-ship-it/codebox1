#include <stdio.h>

int main() {
    // 定义两个数组，各存 8 个 double
    double arr1[8], arr2[8];  

    printf("请输入 8 个 double 类型数值：\n");
    for (int i = 0; i < 8; i++) {
        // 输入第一个数组
        scanf("%lf", &arr1[i]);  
        // 计算累积和：arr2[i] = arr1[0] + arr1[1] + ... + arr1[i]
        // 利用前一次结果，避免嵌套循环
        arr2[i] = (i == 0 ? 0 : arr2[i - 1]) + arr1[i];  
    }

    // 输出第一个数组
    printf("第一个数组：");  
    for (int i = 0; i < 8; i++) {
        printf("%.2f ", arr1[i]);
    }
    printf("\n");

    // 输出第二个数组
    printf("第二个数组：");  
    for (int i = 0; i < 8; i++) {
        printf("%.2f ", arr2[i]);
    }
    printf("\n");

    return 0;
}
