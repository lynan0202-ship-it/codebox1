//  1. 运行：编译后执行，观察输出  
//  2. 预期：  
//     - 错误声明的输出会乱码（因为没 '\0'，printf 会一直读内存直到遇到 '\0'）  
//     - 两个正确声明都输出 "ress"  
//  3. 检查点：对比乱码和正常输出，理解 '\0' 的作用  
// 易错点提醒：  
//  1. 忘记加 '\0' 会导致字符串操作（如 printf、strlen）出错  
// 拓展思考：  
