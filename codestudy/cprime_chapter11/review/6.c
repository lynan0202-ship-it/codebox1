// 题干：6. 假设有如下声明：  
// char sign = '$';  
// 问题：  
// - sign 占用多少字节的内存？  
// - '$' 占用多少字节的内存？  
// - "$" 占用多少字节的内存？  
// 知识要点：  
//  1. **char 变量**：存储单个字符，占 **1 字节**（C 标准强制）。  
//  2. **字符常量**（如 '$'）：类型是 **int**（C 语言规定），所以 `sizeof('$')` 等于 `sizeof(int)`（通常 4 字节，32/64 位系统常见）。  
//  3. **字符串常量**（如 "$"）：自动附加 '\0' 作为结束符，所以占 **字符数 + 1** 字节（此处 1 + 1 = 2 字节）。  
// 总体逻辑：  
//  1. 定义 char 变量 sign，赋值 '$'。  
//  2. 用 `sizeof` 分别计算 `sign`、`'$'`、`"$"` 的大小。  
//  3. 输出结果，验证知识点。  

#include <stdio.h>

int main(void) {
    char sign = '$';  // 定义 char 类型变量，存储字符 '$'
    
    // 计算并输出三个目标的内存大小：
    // 1. sign 是 char 变量 → 占 1 字节
    printf("sign（char 变量）的大小：%zu 字节\n", sizeof(sign));
    
    // 2. '$' 是字符常量 → 类型为 int → 占 sizeof(int) 字节（通常 4 字节）
    printf("'$'（字符常量）的大小：%zu 字节\n", sizeof('$'));
    
    // 3. "$" 是字符串常量 → 包含 '$' 和 '\0' → 占 2 字节
    printf("\"$\"（字符串常量）的大小：%zu 字节\n", sizeof("$"));
    
    return 0;
}  


