//     - 执行：`./6`（Windows 下为 `6.exe`）  
//  2. 预期结果（以常见 32/64 位系统为例）：  
//     sign（char 变量）的大小：1 字节  
//     '$'（字符常量）的大小：4 字节  
//     "$"（字符串常量）的大小：2 字节  
//  3. 检查点：  
//     - 对比字符常量和 char 变量的大小，理解“字符常量是 int 类型”的规则。  
//     - 确认字符串常量的大小包含隐含的 '\0'。  


// ------------------------------
// 易错点提醒：  
//  1. **字符常量的类型误解**：  
//     很多初学者会误以为 `'$'` 是 char 类型，实际 C 标准规定其为 int 类型，因此 `sizeof('$')` 等于 `sizeof(int)`（而非 1 字节）。  
//  2. **字符串结束符遗忘**：  
//     字符串常量 `"$"` 会自动在末尾添加 `'\0'`，所以长度是 **2 字节**（而非 1 字节）。  


