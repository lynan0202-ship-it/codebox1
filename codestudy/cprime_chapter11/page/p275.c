// 题干：实现程序清单11.1的功能，定义字符数组、字符串常量，使用 puts 函数输出字符串，修改字符数组内容并再次输出，理解字符串在 C 语言中的表示与基本操作
// 知识要点：
//  1. 字符数组的初始化：可在定义时用字符串字面量初始化，末尾自动添加 '\0' 作为结束标志 
//  2. 字符串常量：用 const char* 指向存储在只读内存区的字符串，不能修改其内容 
//  3. puts 函数：用于输出字符串，会自动在字符串末尾添加换行符 
//  4. 字符数组元素修改：字符数组属于可修改的内存区域，可通过下标等方式修改单个字符 
// 总体逻辑：
//  1. 定义字符数组、字符串常量以及相关辅助宏和变量 
//  2. 使用 puts 函数依次输出提示信息、字符串常量、字符数组、另一个字符串常量 
//  3. 修改字符数组的某个元素，再次使用 puts 输出修改后的字符数组内容 
//  4. 程序结束返回 0
#include <stdio.h>
#define MSG "I am a symbolic string constant."  // 定义字符串常量宏
#define MAXLENGTH 81  // 定义字符数组最大长度宏

int main(void)
{
    // 定义字符数组并初始化，字符串字面量会自动在末尾加 '\0'
    char words[MAXLENGTH] = "I am a string in an array."; 
    // 定义指向字符串常量的指针，字符串常量存储在只读区域
    const char *pt1 = "Something is pointing at me."; 

    puts("Here are some strings:");  // 输出提示信息
    puts(MSG);  // 输出宏定义的字符串常量，puts 会自动换行
    puts(words);  // 输出字符数组存储的字符串
    puts(pt1);  // 输出字符串常量

    // 修改字符数组的第 9 个元素（下标从 0 开始，words[8] 对应第 9 个字符）
    words[8] = 'p'; 
    puts(words);  // 输出修改后的字符数组内容

    return 0;
}

// 测试验证方案：
//  1. 怎么运行：使用 C 语言编译器（如 gcc，命令为 gcc 文件名.c -o 可执行文件名 ，然后运行生成的可执行文件；或在集成开发环境中直接编译运行 ）
//  2. 预期结果：
//     - 首先输出 "Here are some strings:" 
//     - 接着依次输出 "I am a symbolic string constant." 、"I am a string in an array." 、"Something is pointing at me." 
//     - 最后输出修改后的 "I am a spring in an array."（原 words 数组中第 9 个字符 't' 被改为 'p' ，即 “string” 变为 “spring” ）
//  3. 检查点：
//     - 检查输出的字符串顺序是否正确，是否包含所有预期的字符串内容 
//     - 检查字符数组修改后的输出，确认指定位置字符是否被成功修改 
// 易错点提醒：
//  1. 误修改字符串常量：若尝试对 pt1 指向的字符串常量进行修改（如 pt1[0] = 'A' ），会导致程序崩溃，因为字符串常量存储在只读内存区 
//  2. 字符数组越界：若修改 words 数组元素时，下标超过 MAXLENGTH - 1 ，可能会破坏其他内存区域数据，引发未定义行为 
//  3. 忘记字符串结束标志：字符数组初始化若没用字符串字面量，需手动添加 '\0' ，否则 puts 输出可能会出现乱码，直到遇到内存中的 '\0' 为止 
// 拓展思考：
//  1. 如果想要修改字符串常量的内容，该怎么处理？可以将字符串常量复制到字符数组中，再修改字符数组内容，例如用 strcpy 函数（需包含 <string.h> ）：char temp[MAXLENGTH]; strcpy(temp, pt1); temp[0] = 'N'; puts(temp); 
//  2. 如何统计字符数组中字符串的长度？可以使用 strlen 函数（需包含 <string.h> ），例如 size_t len = strlen(words); printf("Length: %zu\n", len); ，注意 strlen 不包含 '\0' 本身 
//  3. 若不使用 puts 函数，改用 printf 输出字符串，该怎么写？可以用 printf("%s\n", 字符串) ，例如 printf("%s\n", MSG); ，效果和 puts 类似，只是需要手动加换行符 
// 对比说明：
//  - 字符数组 vs 字符串常量：
//    → 字符数组在可读写内存区，可修改元素内容；字符串常量在只读内存区，不能修改，修改会触发未定义行为 
//    → 字符数组使用时直接操作数组名或元素，字符串常量通过指针间接访问，本质都是基于字符指针操作内存 
//  - puts 函数 vs printf 函数输出字符串：
//    → puts 自动添加换行符，简洁方便输出字符串；printf 更灵活，可控制格式，但输出字符串需手动加换行符（如 %s\n ） 
//    → puts 遇到 '\0' 就停止输出，printf 输出 %s 时同样遇到 '\0' 停止，二者在处理字符串结束标志上逻辑一致 