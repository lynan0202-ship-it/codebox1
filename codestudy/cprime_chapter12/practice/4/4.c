// 题干：在一个循环中编写并测试一个函数，该函数返回它被调用的次数。  
// 知识要点：  
//  1. 静态局部变量（static）：函数内的静态变量，调用后值保留  
//  2. 函数返回值：每次调用返回递增的计数  
// 总体逻辑：  
//  1. 定义函数 count_call()，内部用 static int 记录调用次数  
//  2. 主函数循环调用该函数，打印每次的返回值（验证计数）  
#include <stdio.h>  

// 统计自身被调用次数的函数  
int count_call(void) {  
    static int count = 0; // 静态局部变量，仅第一次初始化，之后值保留  
    count++;              // 每次调用，计数+1  
    return count;         // 返回当前计数  
}  

int main() {  
    printf("测试函数调用次数：\n");  
    for (int i = 0; i < 5; i++) { // 循环调用5次  
        int call_num = count_call();  
        printf("第 %d 次调用，函数返回：%d\n", i+1, call_num);  
    }  
    return 0;  
}  

// 测试验证方案：  
//  1. 怎么运行：编译后直接运行（如 `gcc 4.c -o 4 && ./4`）  
//  2. 预期结果：依次输出 1、2、3、4、5（每次调用计数递增）  
//  3. 检查点：看返回值是否连续递增，验证静态变量的“记忆”功能  
// 易错点提醒：  
//  1. 误以为 static 变量会被多次初始化（实际仅第一次）  
//  2. 把 static 变量写成普通局部变量（会导致每次重置为0，返回值永远是1）  
// 拓展思考：  
//  1. 如果多个函数都有 static 变量，会互相影响吗？→ 不会，static 变量作用域是函数内，各自独立  
// 对比说明：  
//  静态局部变量 vs 普通局部变量：  
//  - 普通局部变量：每次调用重新初始化，值不保留  
//  - 静态局部变量：仅第一次初始化，值跨调用保留（本题核心用法）