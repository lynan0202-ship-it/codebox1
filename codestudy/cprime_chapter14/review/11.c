// 题干：声明一个标记为choices的枚举，把枚举常量no、yes和maybe分别设置为0、1、2。
// 知识要点：
//  1. 枚举（enum）的定义与使用
//  2. 枚举常量的显式赋值（可指定枚举常量对应的整数值）
// 总体逻辑：
//  1. 定义枚举类型`enum choices`，包含`no`、`yes`、`maybe`三个枚举常量，分别显式赋值为0、1、2
//  2. 在`main`函数中，打印每个枚举常量的值，验证是否与设置的0、1、2一致
#include <stdio.h>

// 定义枚举类型enum choices，显式指定每个枚举常量的值
enum choices {
    no = 0,     // 将枚举常量no的值设为0
    yes = 1,    // 将枚举常量yes的值设为1
    maybe = 2   // 将枚举常量maybe的值设为2
};

int main() {
    // 打印枚举常量no的值，验证是否为0
    printf("no的值：%d\n", no);
    // 打印枚举常量yes的值，验证是否为1
    printf("yes的值：%d\n", yes);
    // 打印枚举常量maybe的值，验证是否为2
    printf("maybe的值：%d\n", maybe);
    
    return 0;
}

