//  1. 怎么运行：使用C编译器（如gcc）编译代码（例如命令：gcc enum_choices.c -o enum_choices），然后运行生成的可执行文件（例如命令：./enum_choices）
//  2. 预期结果：程序输出三行内容，分别是“no的值：0”、“yes的值：1”、“maybe的值：2”
//  3. 检查点：重点查看每个枚举常量对应的输出数值是否与题目要求的0、1、2完全一致
// 易错点提醒：
//  1. 若忘记显式给枚举常量赋值，系统会自动从0开始递增赋值（即no默认0、yes默认1、maybe默认2），但题目要求显式设置，所以最好显式写`= 值`，避免依赖默认行为导致的潜在误解
//  2. 枚举常量是“常量”，不能在程序中被赋值修改（比如写`no = 5;`会触发编译错误）
