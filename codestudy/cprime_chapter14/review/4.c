// 题干：定义一个数组，内含 12 个结构（第 3 题的结构类型）并初始化为一个年份（非闰年）。
// 知识要点：
//  1. 结构体的定义与复用 - 使用第3题的月份结构体，把月份相关数据（名称、缩写、天数、月份号）“打包”
//  2. 结构体数组的定义与初始化 - 用大括号按顺序给数组中每个结构体元素赋初值
//  3. 非闰年月份天数规则 - 1、3、5、7、8、10、12月有31天；4、6、9、11月有30天；2月有28天
// 总体逻辑：
//  1. 先定义第3题的月份结构体（存名称、缩写、天数、月份号）
//  2. 定义包含12个该结构体的数组，按1-12月的顺序，逐个初始化每个月份的名称、缩写、天数、月份号
//  3. 在main函数中循环打印每个月份的信息，验证初始化是否正确
#include <stdio.h>

// 定义月份结构体（和第3题结构一致）
struct Month {
    char name[20];   // 存完整的月份名称，预留足够字符空间（包含字符串结束符）
    char abbrev[4];  // 存3个字母的缩写，需要4个字符空间（3个字母 + 字符串结束符'\0'）
    int days;        // 存该月的天数
    int monthNum;    // 存月份号（1到12）
};

int main() {
    // 定义结构体数组并初始化，按1-12月的顺序依次设置每个月份的信息
    struct Month months[12] = {
        {"January", "Jan", 31, 1},    // 1月
        {"February", "Feb", 28, 2},   // 非闰年的2月，天数为28
        {"March", "Mar", 31, 3},      // 3月
        {"April", "Apr", 30, 4},      // 4月
        {"May", "May", 31, 5},        // 5月
        {"June", "Jun", 30, 6},       // 6月
        {"July", "Jul", 31, 7},       // 7月
        {"August", "Aug", 31, 8},     // 8月
        {"September", "Sep", 30, 9},  // 9月
        {"October", "Oct", 31, 10},   // 10月
        {"November", "Nov", 30, 11},  // 11月
        {"December", "Dec", 31, 12}   // 12月
    };

    // 循环打印12个月份的信息，检查初始化结果
    for (int i = 0; i < 12; i++) {
        printf("月份号：%d，名称：%s，缩写：%s，天数：%d\n", 
               months[i].monthNum,   // 访问“月份号”成员
               months[i].name,       // 访问“完整名称”成员
               months[i].abbrev,     // 访问“3字母缩写”成员
               months[i].days);      // 访问“天数”成员
    }

    return 0;
}

// 测试验证方案：
//  1. 怎么运行：把代码保存为.c文件（比如叫year_months.c），用C语言编译器（如gcc）编译：`gcc year_months.c -o year_months`，然后运行生成的可执行文件`./year_months`
//  2. 预期结果：程序会输出12行内容，每行对应一个月份，比如第一行是“月份号：1，名称：January，缩写：Jan，天数：31”，第二行是“月份号：2，名称：February，缩写：Feb，天数：28”，且所有月份的天数都符合非闰年规则
//  3. 检查点：重点看每个月份的天数是否正确（2月是否为28，1、3、5等大月是否为31，4、6等小月是否为30），以及名称、缩写、月份号是否和初始化的一致
// 易错点提醒：
//  1. 结构体数组初始化顺序容易错 - 必须严格按照1到12月的顺序初始化，否则月份号和天数会对应错误（比如把3月的天数赋给2月）
//  2. 字符数组长度容易设小 - 像“September”这样的长月份名，`name[20]`要确保能装下；缩写的`abbrev[4]`如果设成3，就存不下字符串结束符，会导致打印异常
// 拓展思考：
//  1. 要是支持闰年，怎么改代码？可以加一个变量标记是否为闰年（比如`int isLeap = 1;`表示闰年），然后2月的天数初始化时写`isLeap ? 29 : 28`
//  2. 如何让用户输入月份号，然后显示该月的信息？可以写代码让用户输入1-12的数字，然后用循环遍历结构体数组，找到`monthNum`等于输入值的元素，打印它的成员
// 对比说明：
//  用结构体数组存12个月，比用四个单独的数组（分别存名称、缩写、天数、月份号）更好用：结构体数组把每个月的所有信息“捆”在一个元素里，通过一个下标就能拿到该月的所有数据，代码读起来更清楚，维护也更方便。
