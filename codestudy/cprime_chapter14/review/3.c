// 题干：设计一个结构模板储存一个月份名、该月份名的3个字母缩写、该月的天数以及月份号。
// 知识要点：
//  1. C语言结构体的定义 - 可包含不同类型的成员（字符数组、整型），将相关数据“打包”
//  2. 结构体变量的初始化 - 用大括号按成员顺序为结构体变量赋初值
//  3. 结构体成员的访问 - 通过“.”操作符访问结构体变量的各个成员
// 总体逻辑：
//  1. 定义结构体`Month`，包含存储月份名、3字母缩写的字符数组，以及存储天数、月份号的整型成员
//  2. 在`main`函数中定义`struct Month`类型的变量并初始化（以1月为例）
//  3. 打印该变量的各个成员，验证结构体是否正确存储数据
#include <stdio.h>

// 定义结构体Month，包含四个成员，分别存月份名、缩写、天数、月份号
struct Month {
    char name[20];     // 存储完整月份名，预留足够字符空间（含字符串结束符）
    char abbrev[4];    // 存储3个字母缩写，需4个字符空间（3个字母 + 字符串结束符'\0'）
    int days;          // 存储该月的天数
    int monthNum;      // 存储月份号（1 - 12）
};

int main() {
    // 定义并初始化结构体变量jan，对应1月（January）
    struct Month jan = {"January", "Jan", 31, 1};

    // 打印jan的完整月份名
    printf("月份名：%s\n", jan.name);
    // 打印jan的3字母缩写
    printf("3字母缩写：%s\n", jan.abbrev);
    // 打印jan的天数
    printf("天数：%d\n", jan.days);
    // 打印jan的月份号
    printf("月份号：%d\n", jan.monthNum);

    return 0;
}

// 测试验证方案：
//  1. 怎么运行：将代码保存为.c文件（如month_struct.c），用C编译器（如gcc）编译：`gcc month_struct.c -o month_struct`，然后运行`./month_struct`
//  2. 预期结果：程序输出四行内容，依次为“月份名：January”、“3字母缩写：Jan”、“天数：31”、“月份号：1”
//  3. 检查点：查看每个成员的输出是否与初始化的值一致，确保结构体存储和访问正确
// 易错点提醒：
//  1. 字符数组长度易设错：比如`abbrev`若设为`char abbrev[3];`，存储"Jan"时没有空间放字符串结束符'\0'，会导致字符串操作异常，要保证数组长度≥字符串长度+1
//  2. 初始化顺序易出错：结构体成员初始化的顺序必须和结构体定义时的成员顺序一致，否则会把值赋错（比如把天数赋给月份号）
// 拓展思考：
//  1. 若要存储一年12个月的信息，如何扩展？可定义结构体数组`struct Month year[12];`，然后循环或逐个为每个数组元素（每个月份）初始化
//  2. 如何实现“根据用户输入的月份号，输出对应月份信息”？可结合结构体数组和分支/循环，让用户输入月份号后，查找数组中对应元素并打印成员
// 对比说明：
//  用结构体存储相关数据（如月份的名、缩写、天数、号），比单独用多个分散的变量（如多个char数组、多个int变量）更便捷：结构体将这些数据“封装”成一个整体，访问和传递时只需操作一个结构体变量，代码更简洁易维护。
