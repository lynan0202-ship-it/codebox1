// 知识要点：
//  1. 字符串比较函数 `strcmp` 的使用（需包含 `<string.h>`，比较两个字符串是否相等）
//  2. 字符数组与字符串的存储（用二维数组存十二个月的英文名称）
//  3. 函数的定义、调用与返回值（编写计算总天数的自定义函数）
//  4. 数组的遍历与累加（遍历月份数组找匹配，遍历天数数组累加总天数）
//  5. 条件判断（用 `strcmp` 结果判断月份是否匹配）
// 总体逻辑：
//  1. 定义 `getTotalDays` 函数，接收月份名字符串，返回总天数
//  2. 函数内定义“月份名称数组”和“每月天数数组”（非闰年，二月按28天）
//  3. 遍历月份名称数组，用 `strcmp` 找与输入匹配的月份，找到后累加该月及之前所有月份的天数
//  4. 主函数中提示用户输入月份名，调用 `getTotalDays`，根据返回值输出结果（正确或错误提示）
#include <stdio.h>
#include <string.h>  // 包含 strcmp 所需的头文件

// 函数：根据月份名计算到该月为止的总天数
int getTotalDays(char monthName[]) {
    // 存储12个月的英文名称
    char months[12][10] = {"January", "February", "March", "April", "May", "June", 
                          "July", "August", "September", "October", "November", "December"};
    // 存储每个月的天数（非闰年情况）
    int days[12] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
    int totalDays = 0;  // 总天数初始化为0
    int i;

    // 遍历月份名称数组，查找匹配的月份
    for (i = 0; i < 12; i++) {
        // strcmp返回0表示两个字符串相等
        if (strcmp(monthName, months[i]) == 0) {
            // 找到匹配的月份，累加从1月到该月的天数
            for (int j = 0; j <= i; j++) {
                totalDays += days[j];
            }
            break;  // 找到后退出循环，避免多余遍历
        }
    }

    return totalDays;  // 返回总天数（若没找到，totalDays仍为0）
}

int main() {
    char inputMonth[10];  // 存储用户输入的月份名，假设长度不超过9个字符
    printf("请输入月份的英文名称（例如 January）：");
    scanf("%s", inputMonth);  // 输入月份名（无空格，用%s读取）

    int result = getTotalDays(inputMonth);  // 调用函数计算总天数

    // 根据结果判断输出
    if (result == 0) {
        printf("输入的月份名不存在，请检查拼写！\n");
    } else {
        printf("到 %s 为止（包括该月）的总天数是：%d\n", inputMonth, result);
    }

    return 0;
}

