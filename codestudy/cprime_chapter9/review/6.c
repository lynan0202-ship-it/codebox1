// 题干：设计一个名为 alter() 的函数，接受两个 int 类型的变量 x 和 y，把它们的值分别改成两个变量之和以及两变量之差。
// 知识要点：
//  1. 函数参数传递（指针传递，实现修改实参的值）
//  2. 基本算术运算（加法和减法）
//  3. 指针的解引用操作（通过指针访问和修改变量的值）
// 总体逻辑：
//  1. 定义 `alter` 函数，接收两个 int 类型指针参数
//  2. 在函数内部，先计算两数的和与差，再通过解引用指针将和与差分别赋值给原来的两个变量
//  3. 在 `main` 函数中定义两个变量，调用 `alter` 函数，最后输出修改后的变量值
#include <stdio.h>

// 定义 alter 函数，接收两个 int 类型指针，用于修改实参的值
void alter(int *x, int *y) {
    // 保存原来 x 指针指向的值
    int original_x = *x; 
    // 计算两数之和，赋值给 x 指针指向的变量（即原来的 x 变量）
    *x = *x + *y; 
    // 计算两数之差（用原来的 x 值减去 y 值），赋值给 y 指针指向的变量（即原来的 y 变量）
    *y = original_x - *y; 
}

int main() {
    // 定义两个初始变量
    int x = 5, y = 3; 
    printf("调用 alter 函数前：x = %d，y = %d\n", x, y);

    // 调用 alter 函数，传递 x 和 y 的地址，实现修改实参
    alter(&x, &y); 

    printf("调用 alter 函数后：x = %d，y = %d\n", x, y);
    return 0;
}

// 测试验证方案：
//  1. 怎么运行：
//     - 保存代码为 alter.c
//     - 用 gcc 编译：gcc alter.c -o alter
//     - 运行：./alter（Linux/Mac） 或 alter.exe（Windows）
//  2. 预期结果：
//     - 调用 alter 函数前输出：调用 alter 函数前：x = 5，y = 3
//     - 调用 alter 函数后输出：调用 alter 函数后：x = 8，y = 2（5 + 3 = 8，5 - 3 = 2 ）
//  3. 检查点：
//     - 确认 x 最终值为两数之和，y 最终值为两数之差
// 易错点提醒：
//  1. 容易在计算差的时候，直接使用 *x - *y，这样会因为 *x 已经被修改为和，导致结果错误，需要用原来的 x 值计算，所以要提前保存 original_x
