#include <stdio.h>

// 原函数存在的问题分析及正确写法：
// 问题 1：参数 num 没有声明类型（旧风格不推荐，现代 C 要求明确参数类型）
// 问题 2：函数内定义的局部变量 num 与参数 num 同名，造成作用域冲突
// 问题 3：for 循环中 num++ 会导致循环条件 `count <= num` 无法正确控制循环次数（num 不断变大，可能死循环）
// 以下是正确的函数定义
void salami(int num) {
    // 用不同的变量名，避免冲突
    int count; 
    // 修正 for 循环，用 count 作为循环变量更新
    for (count = 1; count <= num; count++) { 
        printf(" O salami mio!\n");
    }
}

int main() {
    // 调用函数测试，传入参数 3，期望打印 3 次
    salami(3); 
    return 0;
}

