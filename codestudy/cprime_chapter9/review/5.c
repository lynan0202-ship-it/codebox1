//  1. 怎么运行：
//     - 保存代码为 sum_double.c
//     - 用 gcc 编译：gcc sum_double.c -o sum_double 
//     - 运行：./sum_double（Linux/Mac） 或 sum_double.exe（Windows）
//  2. 预期结果：
//     - 输出类似 “准备计算 2.5 和 3.7 的和” 以及 “2.5 和 3.7 的和是：6.20”（具体小数位数看输出控制）
//  3. 检查点：
//     - 手动计算两数之和（2.5 + 3.7 = 6.2 ），对比程序输出结果是否一致 
// 易错点提醒：
//  1. 容易忘记修改函数返回值类型，仍写成 int，导致返回结果被截断（比如 6.2 会变成 6 ）
//  2. 定义变量、调用函数时，类型要保持 double 一致，否则可能因类型转换出现精度丢失 
