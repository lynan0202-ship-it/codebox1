// 题干：如果把复习题 4 改成返回两个 double 类型的值之和，应如何修改函数？
// 知识要点：
//  1. 函数返回值类型的修改（从 int 改为 double）
//  2. double 类型数据的运算与处理 
//  3. 函数参数、返回值类型匹配的重要性
// 总体逻辑：
//  1. 将原函数的参数类型、返回值类型从 int 调整为 double 
//  2. 保证函数内部运算、返回值以及调用处的变量类型都与 double 匹配 
//  3. 演示修改后函数的定义、调用与结果输出 
#include <stdio.h>

// 定义函数：返回两个 double 类型数的和
// 形参 a、b 改为 double 类型，接收实参传递的双精度浮点数
double sum_double(double a, double b) {
    // 同样通过加法运算计算两数之和，返回 double 类型结果
    return a + b; 
}

int main() {
    // 定义两个 double 类型的数（实参），可包含小数
    double num1 = 2.5, num2 = 3.7; 
    printf("准备计算 %.1f 和 %.1f 的和\n", num1, num2);

    // 调用 sum_double 函数，接收返回的 double 类型结果
    double result = sum_double(num1, num2); 

    // %.2f 控制输出保留两位小数，展示 double 类型的运算结果
    printf("%.1f 和 %.1f 的和是：%.2f\n", num1, num2, result);
    return 0;
}

