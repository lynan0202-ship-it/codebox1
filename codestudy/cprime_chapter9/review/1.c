// 题干：实际参数和形式参数的区别是什么？
// 知识要点：
//  1. 函数参数传递的基本概念
//  2. 实际参数（实参）和形式参数（形参）在程序运行中的角色
// 总体逻辑：
//  1. 定义一个简单函数来演示实参和形参
//  2. 在 main 函数中调用该函数，展示实参传递给形参的过程
//  3. 解释实参是调用时传入的值，形参是函数定义中接收值的变量
#include <stdio.h>

// 定义一个函数，形参 num 用来接收实参的值
void demoFunction(int num) {
    // 这里的 num 是形式参数，是实参的一份拷贝
    printf("在函数内部，形式参数 num 的值：%d\n", num);
}

int main() {
    // 定义实际参数，即调用函数时要传入的值
    int realParam = 10; 
    printf("准备调用函数，实际参数 realParam 的值：%d\n", realParam);
    
    // 调用函数，将实际参数 realParam 的值传递给形式参数 num
    demoFunction(realParam); 

    // 总结区别：
    // 实参是调用函数时实际传入的数据，如这里的 realParam
    // 形参是函数定义中用来接收实参值的变量，如 demoFunction 里的 num
    // 形参是实参的拷贝，函数内修改形参不影响实参
    return 0;
}

