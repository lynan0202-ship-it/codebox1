// 题干：编写一个函数，返回 3 个整数参数中的最大值。
// 知识要点：
//  1. 函数的定义（返回值类型、多参数的处理）
//  2. 条件判断（if-else 语句用于比较大小）
//  3. 函数返回值的使用（将最大值返回给调用处 ）
// 总体逻辑：
//  1. 定义 `max_of_three` 函数，接收三个 int 类型参数
//  2. 通过多次比较（if-else ），找出三个数中的最大值
//  3. 在 `main` 函数中定义三个整数，调用函数并输出最大值
#include <stdio.h>

// 定义函数，返回三个 int 中的最大值
int max_of_three(int a, int b, int c) {
    // 先假设 a 是最大值
    int max = a; 
    // 如果 b 大于当前 max，更新 max 为 b
    if (b > max) { 
        max = b;
    }
    // 如果 c 大于当前 max，更新 max 为 c
    if (c > max) { 
        max = c;
    }
    // 返回找到的最大值
    return max; 
}

int main() {
    // 定义三个测试用的整数
    int num1 = 3, num2 = 7, num3 = 5; 
    printf("三个数分别是：%d, %d, %d\n", num1, num2, num3);

    // 调用函数，获取最大值
    int result = max_of_three(num1, num2, num3); 

    printf("最大值是：%d\n", result);
    return 0;
}

