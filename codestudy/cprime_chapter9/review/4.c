// 题干：设计一个函数，返回两整数之和。
// 知识要点：
//  1. 函数的定义（包含返回值类型、函数名、参数列表）
//  2. 基本数据类型（int）的运算与返回值使用 
//  3. 函数调用时的参数传递（值传递）
// 总体逻辑：
//  1. 定义一个名为 sum_int 的函数，接收两个 int 类型参数
//  2. 在函数内部通过加法运算得到两数之和并返回 
//  3. 在 main 函数中定义两个整数，调用 sum_int 函数并输出结果 
#include <stdio.h>

// 定义函数：返回两个 int 类型数的和
// 形参 a、b 用于接收调用时传入的实参值
int sum_int(int a, int b) {
    // 通过加法运算计算两数之和并返回结果
    return a + b; 
}

int main() {
    // 定义两个实际要相加的整数（实参）
    int num1 = 5, num2 = 3; 
    printf("准备计算 %d 和 %d 的和\n", num1, num2);

    // 调用 sum_int 函数，将 num1、num2 作为实参传递，接收返回的和
    int result = sum_int(num1, num2); 

    printf("%d 和 %d 的和是：%d\n", num1, num2, result);
    return 0;
}

