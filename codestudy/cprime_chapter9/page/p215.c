#include <stdio.h>

// 函数原型声明：告诉编译器 imin 函数接收两个 int 类型参数，返回 int 类型结果
int imin(int n, int m);  

int main(void) 
{
    int evil1, evil2;  // 定义两个变量，用于存储用户输入的两个整数

    // 提示用户输入，告知退出方式
    printf("Enter a pair of integers (q to quit):\n");
    // 当 scanf 成功读取两个整数（返回值为 2 ）时，进入循环
    while (scanf("%d %d", &evil1, &evil2) == 2) 
    {
        // 调用 imin 函数，传入 evil1 和 evil2，接收返回的较小值并输出结果
        printf("The lesser of %d and %d is %d.\n", evil1, evil2, imin(evil1, evil2));
        // 提示用户继续输入或退出
        printf("Enter a pair of integers (q to quit):\n");
    }
    // 循环终止（输入非整数或 q 等 ），输出结束提示
    printf("Bye.\n");  

    return 0;  // main 函数返回 0，程序正常结束
}

// 函数定义：接收两个 int 类型参数 n 和 m，返回其中较小的值
int imin(int n, int m) 
{
    // 如果 n 小于 m，返回 n；否则返回 m，实现找较小值功能
    if (n < m) 
    {
        return n;  
    } 
    else 
    {
        return m;  
    }
}

