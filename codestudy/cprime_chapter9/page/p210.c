#include <stdio.h>   // 引入标准输入输出库，这样才能使用 printf、putchar 等函数
#define NAME "GIGATHINK, INC."   // 定义公司名称常量
#define ADDRESS "101 Megabuck Plaza"   // 定义公司地址常量
#define PLACE "Megapolis, CA 94904"   // 定义公司所在地点常量
#define WIDTH 40   // 定义星号栏的宽度，即一行打印多少个星号

// 函数原型声明，告诉编译器后续会有一个这样的函数定义，返回值 void（无返回值），无参数
void starbar(void);  

int main(void) 
{
    starbar();   // 调用 starbar 函数，打印开头的星号分隔栏
    printf("%s\n", NAME);   // 打印公司名称
    printf("%s\n", ADDRESS);   // 打印公司地址
    printf("%s\n", PLACE);   // 打印公司所在地点
    starbar();   // 再次调用 starbar 函数，打印结尾的星号分隔栏
    return 0;   // main 函数返回 0，表示程序正常结束
}

// 函数定义：实现打印一行 WIDTH 个星号并换行的功能，返回值 void（无返回值），参数 void（无参数）
void starbar(void) 
{
    int count;   // 定义局部变量 count，用来控制循环次数
    // for 循环初始化 count 为 1，只要 count 小于等于 WIDTH 就执行循环体，每次循环 count 自增 1
    for (count = 1; count <= WIDTH; count++) 
    {
        putchar('*');   // 每次循环打印一个星号
    }
    putchar('\n');   // 星号打印完后，打印换行符，让后续内容另起一行
}

