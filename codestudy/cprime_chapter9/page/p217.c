#include <stdio.h>

// 这里演示函数原型声明放在主调函数外的写法（全局作用域 ），也可像下面 main 里那样放在内部
// int imin(int, int); 

int main(void) 
{
    // 在主调函数内部声明函数原型，只要在调用前声明即可
    int imin(int, int);  

    int num1, num2, result;

    printf("请输入两个整数，用空格分隔：");
    // 输入两个整数，存入 num1 和 num2
    scanf("%d %d", &num1, &num2);  

    // 调用 imin 函数，实参 num1、num2 传递给形参，接收返回的较小值
    result = imin(num1, num2);  
    printf("较小的整数是：%d\n", result);

    return 0;
}

// 函数定义：实现找出两个整数中较小值的功能，返回值类型 int，参数是两个 int 类型
int imin(int a, int b) 
{
    return (a < b) ? a : b;  
}

