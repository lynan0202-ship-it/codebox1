// 题干：编写一个函数，从标准输入中读取字符，直到遇到文件结尾。程序要报告每个字符是否是字母。如果是，还要报告该字母在字母表中的数值位置。合并一个函数，以一个字符作为参数，如果该字符是一个字母则返回一个数值位置，否则返回 -1。
// 知识要点：
//  1. 字符输入输出（`getchar` 读取字符、`printf` 输出结果 ）
//  2. 函数定义与调用（实现字符判断和位置计算的功能函数 ）
//  3. 字符分类与 ASCII 码运用（判断字母及计算在字母表中的位置 ）
// 总体逻辑：
//  1. 定义 `char_position` 函数，接收一个字符参数，判断是否为字母并返回对应位置或 -1 
//  2. 在 `main` 函数中，循环使用 `getchar` 读取标准输入字符，调用 `char_position` 函数获取结果并输出
#include <stdio.h>

// 定义函数，判断字符是否为字母并返回其在字母表中的位置，非字母返回 -1
int char_position(char ch) {
    // 判断是否为小写字母
    if (ch >= 'a' && ch <= 'z') { 
        // 小写字母 'a' 对应位置 1，通过与 'a' 做差计算位置
        return ch - 'a' + 1; 
    } 
    // 判断是否为大写字母
    else if (ch >= 'A' && ch <= 'Z') { 
        // 大写字母 'A' 对应位置 1，通过与 'A' 做差计算位置
        return ch - 'A' + 1; 
    } 
    // 非字母情况
    else { 
        return -1;
    }
}

int main() {
    char ch;
    // 循环读取标准输入字符，直到遇到文件结尾（EOF，在终端可通过 Ctrl + D 输入 ）
    while ((ch = getchar()) != EOF) { 
        int pos = char_position(ch);
        if (pos != -1) {
            printf("字符 '%c' 是字母，在字母表中的位置是 %d\n", ch, pos);
        } else {
            printf("字符 '%c' 不是字母\n", ch);
        }
    }
    return 0;
}

