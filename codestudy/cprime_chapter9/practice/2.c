// 题干：设计一个函数 chline(ch, i, j)，打印指定的字符 j 行 i 列。在一个简单的驱动程序中测试该函数。
// 知识要点：
//  1. 函数的定义（多参数传递，包括字符和整数）
//  2. 嵌套循环（控制行和列的打印）
//  3. 字符的输出
// 总体逻辑：
//  1. 定义 `chline` 函数，接收一个字符参数和两个整数参数（分别表示列数和行数 ）
//  2. 使用嵌套循环，外层循环控制行数，内层循环控制列数，打印指定字符
//  3. 在 `main` 函数中调用 `chline` 函数，传入不同的参数进行测试
#include <stdio.h>

// 定义 chline 函数，打印字符 ch，共 j 行 i 列
void chline(char ch, int i, int j) {
    // 外层循环控制行数，执行 j 次
    for (int row = 0; row < j; row++) { 
        // 内层循环控制列数，执行 i 次
        for (int col = 0; col < i; col++) { 
            // 打印指定字符，不换行
            printf("%c", ch); 
        }
        // 每行结束后换行
        printf("\n"); 
    }
}

int main() {
    printf("打印字符 '*'，3 行 5 列：\n");
    // 调用 chline 函数，打印 '*'，3 行 5 列
    chline('*', 5, 3); 

    return 0;
}

