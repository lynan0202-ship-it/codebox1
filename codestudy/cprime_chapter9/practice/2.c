//  1. 怎么运行：
//     - 保存代码为 chline_function.c
//     - 用 gcc 编译：gcc chline_function.c -o chline_function
//     - 运行：./chline_function（Linux/Mac） 或 chline_function.exe（Windows）
//  2. 预期结果：
//     - 输出 3 行，每行 5 个 '*' 字符
//  3. 检查点：
//     - 确认打印的行数和列数与传入的参数一致
// 易错点提醒：
//  1. 嵌套循环中容易搞混外层循环和内层循环的控制变量，导致行数和列数颠倒
//  2. 注意 `printf` 中字符的输出格式，确保不换行和换行的逻辑正确
