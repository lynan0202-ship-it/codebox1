// 题干：编写一个函数，接受 3 个参数：一个字符和两个整数。字符参数是待打印的字符，第 1 个整数指定一行中打印字符的次数，
//       第 2 个整数指定打印指定字符的行数。编写一个调用该函数的程序。
// 知识要点：
//  1. 函数的多参数传递
//  2. 嵌套循环的使用（控制行和列 ）
//  3. 函数的调用和参数匹配
// 总体逻辑：
//  1. 定义 `printChar` 函数，接收一个字符参数和两个整数参数（分别表示列数和行数 ）
//  2. 使用嵌套循环，外层循环控制行数，内层循环控制列数，打印指定字符
//  3. 在 `main` 函数中调用 `printChar` 函数，传入不同的参数进行测试
#include <stdio.h>

// 定义 printChar 函数，打印字符 ch，每行打印 count 次，共 rows 行
void printChar(char ch, int count, int rows) {
    // 外层循环控制行数
    for (int i = 0; i < rows; i++) { 
        // 内层循环控制每行打印的字符次数
        for (int j = 0; j < count; j++) { 
            printf("%c", ch);
        }
        // 每行结束后换行
        printf("\n"); 
    }
}

int main() {
    printf("打印字符 '#'，每行 4 次，共 3 行：\n");
    // 调用 printChar 函数，打印 '#'，每行 4 次，共 3 行
    printChar('#', 4, 3); 

    return 0;
}

