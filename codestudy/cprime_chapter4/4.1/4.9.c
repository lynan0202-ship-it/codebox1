/* 
flags.c -- 演示 printf 的格式标记（按教程思路注释）
核心目标：用具体例子理解 %x/%X/%#x、空格、精度、0 填充的规则 
*/
#include <stdio.h>

int main(void)
{
    // 1. 演示十六进制格式：%x %X %#x
    //    - %x：小写十六进制（无前缀）
    //    - %X：大写十六进制（无前缀）
    //    - %#x：带 0x 前缀的小写十六进制 
    printf("%x %X %#x\n", 31, 31, 31);

    // 2. 演示“空格填充”与“符号对齐”：
    //    - %d：普通十进制（正数无符号，负数带 -）
    //    - % d：正数前面加空格，负数前面保持 -（让正负值对齐）
    printf("**%d** % d** %d**\n", 42, 42, -42);

    // 3. 演示“字段宽度”“精度”“0 填充”的组合：
    //    - %5d：宽度 5，默认空格填充（右对齐）
    //    - %5.3d：宽度 5，精度 3（不足补前导 0，仍右对齐）
    //    - %05d：宽度 5，用 0 填充（替代空格）
    //    - %05.3d：宽度 5，精度 3，用 0 填充（精度优先，再补 0）
    printf("**%5d**%5.3d**%05d**%05.3d**\n", 6, 6, 6, 6);

    return 0;
}