//赋值运算符（7 个 ，基础 = 、复合赋值 ）
#include <stdio.h>
int main() {
    int a = 10;

    // 1. 基础赋值 = 
    // 功能：把右边值赋给左边变量 
    int b; 
    b = a; 
    printf("b = a 后，b = %d\n", b); // b=10 

    // 2. 复合赋值 += 
    // 功能：a = a + 5 ，等价写法 
    a += 5; 
    printf("a += 5 后，a = %d\n", a); // a=15 

    // 3. 复合赋值 -= 
    // 功能：a = a - 3 ，等价写法 
    a -= 3; 
    printf("a -= 3 后，a = %d\n", a); // a=12 

    // 4. 复合赋值 *= 
    // 功能：a = a * 2 ，等价写法 
    a *= 2; 
    printf("a *= 2 后，a = %d\n", a); // a=24 

    // 5. 复合赋值 /= 
    // 功能：a = a / 4 ，等价写法（整数除法，截断小数 ）
    a /= 4; 
    printf("a /= 4 后，a = %d\n", a); // a=6（24÷4=6 ）

    // 6. 复合赋值 %= 
    // 功能：a = a % 5 ，等价写法（取余 ）
    a %= 5; 
    printf("a %= 5 后，a = %d\n", a); // a=1（6÷5 余 1 ）

    // 7. 复合赋值（拓展，比如 <<= 等，这里演示 += 用于浮点 ）
    float f = 3.5; 
    f += 1.2; 
    printf("f += 1.2 后，f = %.2f\n", f); // f=4.70 

    return 0;
}