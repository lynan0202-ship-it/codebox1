#include <stdio.h>

int main() {
    // 题目 1：所有变量类型为 int
    int x, y;

    // 1.a: x = (12 + 6) / 2 * 3
    // 计算步骤：(18) / 2 = 9 → 9 * 3 = 27
    x = (12 + 6) / 2 * 3;
    printf("1.a: x = %d\n", x); // 输出: 27

    // 1.b: x = (2 + 3) * 6;
    // 计算步骤：5 * 6 = 30
    x = (2 + 3) * 6;
    printf("1.b: x = %d\n", x); // 输出: 30

    // 1.c: y = x = (2 + 3)/4; 
    // 计算步骤：(5)/4 = 1（整数除法，截断小数）→ x=1, y=1
    y = x = (2 + 3) / 4;
    printf("1.c: x = %d, y = %d\n", x, y); // 输出: x=1, y=1

    // 1.d: y = 3 + 2*(x = 7/2); 
    // 计算步骤：7/2 = 3（整数除法）→ x=3 → 2*3=6 → 3+6=9
    y = 3 + 2 * (x = 7 / 2);
    printf("1.d: x = %d, y = %d\n", x, y); // 输出: x=3, y=9

    return 0;
}
/*
1.a: x = 27
1.b: x = 30
1.c: x = 1, y = 1
1.d: x = 3, y = 9
*/