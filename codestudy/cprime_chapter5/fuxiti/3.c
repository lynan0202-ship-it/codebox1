#include <stdio.h>

int main() {
    // 题目 3：表达式求值，注意整数除法（/）和浮点除法（/）的区别
    double result; // 用 double 存储浮点结果

    // 3.a: 30.0 / 4.0 * 5.0
    result = 30.0 / 4.0 * 5.0;
    printf("3.a: 30.0 / 4.0 * 5.0 = %.2f\n", result); // 输出: 37.50

    // 3.b: 30.0 / (4.0 * 5.0)
    result = 30.0 / (4.0 * 5.0);
    printf("3.b: 30.0 / (4.0 * 5.0) = %.2f\n", result); // 输出: 1.50

    // 3.c: 30 / 4 * 5
    // 整数运算：30/4=7（截断小数）→ 7*5=35
    result = 30 / 4 * 5;
    printf("3.c: 30 / 4 * 5 = %.2f\n", result); // 输出: 35.00

    // 3.d: 30 * 5 / 4
    // 整数运算：30*5=150 → 150/4=37（截断小数）
    result = 30 * 5 / 4;
    printf("3.d: 30 * 5 / 4 = %.2f\n", result); // 输出: 37.00

    // 3.e: 30 / 4.0 * 5
    // 浮点运算：30/4.0=7.5 → 7.5*5=37.5
    result = 30 / 4.0 * 5;
    printf("3.e: 30 / 4.0 * 5 = %.2f\n", result); // 输出: 37.50

    // 3.f: 30 / 4 * 5.0
    // 整数运算：30/4=7（截断小数）→ 7*5.0=35.0
    result = 30 / 4 * 5.0;
    printf("3.f: 30 / 4 * 5.0 = %.2f\n", result); // 输出: 35.00

    return 0;
}