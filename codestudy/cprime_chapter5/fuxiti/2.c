#include <stdio.h>

int main() {
    // 题目 2：所有变量类型为 int，涉及浮点数转 int（截断小数）
    int x;

    // 2.a: x = (int)3.8 + 3.3;
    // 计算步骤：(int)3.8 → 3（截断小数）→ 3 + 3.3 → 但 x 是 int，结果取整为 6
    x = (int)3.8 + 3.3;
    printf("2.a: x = %d\n", x); // 输出: 6

    // 2.b: x = (2 + 5) * 10.5;
    // 计算步骤：2+5=7 → 7*10.5=73.5 → 转 int 截断为 73
    x = (2 + 5) * 10.5;
    printf("2.b: x = %d\n", x); // 输出: 73

    // 2.c: x = 3 / 3 * 22.0;
    // 计算步骤：3/3=1（整数除法）→ 1*22.0=22.0 → 转 int 为 22
    x = 3 / 3 * 22.0;
    printf("2.c: x = %d\n", x); // 输出: 22

    // 2.d: x = 22.0 * 3 / 5;
    // 计算步骤：22.0*3=66.0 → 66.0/5=13.2 → 转 int 截断为 13
    x = 22.0 * 3 / 5;
    printf("2.d: x = %d\n", x); // 输出: 13

    return 0;
}/*


2.a: x = 6
2.b: x = 73
2.c: x = 22
2.d: x = 13*/