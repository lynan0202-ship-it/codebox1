#include <stdio.h>

int main() {
    // --------------------- 10.a 部分 ---------------------
    int x = 0;
    // 前置自增：x 先+1，再判断条件
    while (++x < 3) {
        // 条件成立时执行：x=1、2 会进入循环
        printf("10.a: %d\n", x);
    }

    // --------------------- 10.b 部分 ---------------------
    int y = 100;
    // 后置自增：先判断条件，x 再+1
    while (y++ < 103) {
        // 条件成立时执行：y=100、101、102 会进入循环
        printf("10.b: %d\n", y);
    }
    // 循环结束后，y 最终是 104，这里额外打印一次
    printf("10.b (循环外): %d\n", y);

    // --------------------- 10.c 部分 ---------------------
    char ch = 's';
    // 字符比较：'s' ~ 'v' 会进入循环（因为 'w' 不满足）
    while (ch < 'w') {
        // 先打印当前字符，再自增
        printf("10.c: %c\n", ch);
        ch++;
    }
    // 循环结束后，ch 是 'w'，这里额外打印一次
    printf("10.c (循环外): %c\n", ch);

    return 0;
}
/*
10.a: 1
10.a: 2
10.b: 101
10.b: 102
10.b: 103
10.b (循环外): 104
10.c: s
10.c: t
10.c: u
10.c: v
*/