/* 
 * pound.c -- 定义一个带参数的函数，用于演示函数参数传递、类型转换等知识 
 * 教程核心知识点：
 *  1. 函数声明与定义：包含参数的函数如何声明、定义函数头 
 *  2. 形参（形式参数）与实参（实际参数）：调用函数时数据如何传递 
 *  3. 隐式类型转换：不同类型实参传给 int 形参时的自动转换规则 
 *  4. 变量作用域：函数内变量与主函数变量同名不冲突 
 */
#include <stdio.h>

// ANSI 风格函数声明：表明 pound 函数接收一个 int 类型参数，无返回值
void pound(int n);  

// 主函数，程序入口
int main(void)
{
    // 定义不同类型的变量，用于演示参数传递时的类型转换
    int times = 5;    // 整型变量，作为实参传递时直接匹配 int 形参
    char ch = '!';    // 字符型变量，ASCII 码值为 33，传递时会隐式转为 int
    float f = 6.0;    // 浮点型变量，传递时会隐式转为 int（小数部分截断）

    // 1. 传递整型实参：times 是 int 类型，直接传给 pound 的 int 形参 n
    pound(times);     
    // 2. 传递字符型实参：ch 会隐式转换为 ASCII 码对应的 int 值（33），传给形参 n
    pound(ch);        
    // 3. 传递浮点型实参：f 会隐式转换为 int 类型（6.0 转为 6），传给形参 n
    pound(f);         

    return 0;
}

/* 
 * 函数定义：pound 函数，功能是打印指定数量的 # 号 
 * 教程关键解析：
 *  - 函数头 `void pound(int n)` 中，`int n` 是形式参数（形参），
 *    调用函数时，实参会赋值给这个形参，形参是函数的“局部变量” 
 *  - 形参 n 的作用域：仅在 pound 函数内部有效，和 main 函数中变量同名也不冲突 
 */
void pound(int n)  
{
    // 循环打印 # 号：n-- 先判断再自减，直到 n 为 0 停止
    while (n-- > 0)  
        printf("#");
    // 打印换行，让每次调用 pound 输出的 # 号后换行
    printf("\n");    
}